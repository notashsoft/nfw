Hello dear, this is <b>nFW</b>.
a php framework with two strategy:<br/>
1-to use in a small and fast development project, so it enables you to have a controller for getting data and control routines, and a view for styling 
your output, and at the last a layout to style main website layout. it means Model and Controller joined together in controller.<br/>
<br/>
2-second strategy is for when you want to use framework in medium and large scale project, in this situation you enable model too. in this time
every controller has a corresponding model. every model is a simple class for your defined methods. we`re not using entity for simplicity and just sending/getting our data by 
queries.<br/>
